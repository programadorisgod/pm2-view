export function formatUptime(uptimeMs: number): string {
  if (!uptimeMs || uptimeMs <= 0) return 'N/A';

  const seconds = Math.floor(uptimeMs / 1000);
  const minutes = Math.floor(seconds / 60);
  const hours = Math.floor(minutes / 60);
  const days = Math.floor(hours / 24);

  if (days > 0) return `${days}d ${hours % 24}h`;
  if (hours > 0) return `${hours}h ${minutes % 60}m`;
  if (minutes > 0) return `${minutes}m ${seconds % 60}s`;
  return `${seconds}s`;
}

export function formatUptimeFromTimestamp(uptimeTimestamp: number): string {
  if (!uptimeTimestamp || uptimeTimestamp <= 0) return 'N/A';
  const elapsedMs = Date.now() - uptimeTimestamp;
  return formatUptime(elapsedMs);
}

export function formatBytes(bytes: number): string {
  if (bytes === 0) return '0 B';
  const k = 1024;
  const sizes = ['B', 'KB', 'MB', 'GB'];
  const i = Math.floor(Math.log(bytes) / Math.log(k));
  return `${parseFloat((bytes / Math.pow(k, i)).toFixed(2))} ${sizes[i]}`;
}
